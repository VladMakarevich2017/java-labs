package paper;

public class Paper {
	
	protected double length;
	protected double width;
	
	public Paper() {
		
	}
	
	public void setLength(double length) {
		this.length = length;
	}
	
	public double getLength() {
		return length;
	}
	
	public void setWidth(double width) {
		this.width = width;
	}
	
	public double getWidth() {
		return width;
	}

}
